# Loop Through a Tuple

thistuple = ("Rose","Marigold","Mogra")
for i in range(len(thistuple)) :
    print(thistuple[i])
print("_______________________________________")

# Using a While Loop
thistuple = ("Rose","Marigold","Mogra")
cont = 0
while cont < len(thistuple) : 
    print(thistuple[cont])
    cont += 1
print("_______________________________________")




